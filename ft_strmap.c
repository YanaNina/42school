#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char *str;

	str = (char *)malloc(sizeof(*s));
	while (*s)
	{
		*str = f(*s);
		str++;
		s++;
	}
	*str = '\0';
	return (str);
}
