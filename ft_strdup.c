#include "libft.h"

char	*ft_strdup(char *s)
{
	int		i;
	int		j;
	char	*s1;

	i = 0;
	j = 0;
	while (s[i] != '\0')
		i++;
	s1 = (char*)malloc(sizeof(char) * (i + 1));
	while (s[j] != '\0')
	{
		s1[j] = s[j];
		j++;
	}
	return (s1);
}
