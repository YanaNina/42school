#include "libft.h"

char	*ft_strdup(char *s)
{
	int		i;
	char	*s1;

	i = 0;	
	if (!(s1 = (char*)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (0);	
	while (s[i])
	{
		s1[i]  = s[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
