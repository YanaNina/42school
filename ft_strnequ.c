#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!n)
		return (1);
	if (s1 && s2)
	{
		while (n > 0)
		{
			if (*s1 != *s2)
				return (0);
			else
			{
				s1++;
				s2++;
				n--;
			}
		}
		return (1);
	}
	return (0);
}
