#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *s;

	s = (char*)malloc(sizeof(*s) * (size + 1));
	if (s == '\0')
		return (0);
	ft_bzero(s, size + 1);
	return (s);
}
